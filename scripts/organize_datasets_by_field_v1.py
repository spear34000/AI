from __future__ import annotations

import argparse
import json
import re
from collections import Counter, defaultdict
from datetime import datetime, timezone
from pathlib import Path
from typing import Dict, Iterable, List, Tuple


CODE_HINT_RE = re.compile(
    r"(```|def\s+\w+\(|class\s+\w+|import\s+\w+|function\s+\w+|SELECT\s+.+\s+FROM|<\w+[^>]*>)",
    flags=re.IGNORECASE,
)

HANGUL_RE = re.compile(r"[가-힣]")
LETTER_RE = re.compile(r"[A-Za-z가-힣]")


FIELD_DESC = {
    "ko_chat_general": "한국어 일반 대화/질의응답",
    "ko_fluency_targeted": "한국어 유창성/스타일 보강",
    "identity_persona": "모델 정체성/이름/호칭 보정",
    "routing_intent": "질의 라우팅/분기",
    "reasoning_logic": "논리/추론",
    "translation_multilingual": "번역/다국어",
    "coding_tech": "코드/개발 기술",
    "memory_continual": "메모리/지속 학습 버퍼",
    "ccl_specbook": "CCL 스펙/검증",
    "foundation_mit_mix": "대규모 MIT 혼합 코퍼스",
    "stage_curriculum": "단계별 학습 스테이지",
    "quality_curated": "정제 완료 품질셋",
    "profile_meta": "프로파일/메타 데이터",
    "other": "기타",
}


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Organize dataset files into field groups")
    p.add_argument("--data_root", type=Path, default=Path("data"))
    p.add_argument("--out_dir", type=Path, default=Path("data/by_field"))
    p.add_argument("--probe_lines", type=int, default=160)
    p.add_argument(
        "--ext",
        action="append",
        default=[".jsonl", ".json"],
        help="target file extension; can be used multiple times",
    )
    return p.parse_args()


def normalize_space(text: str) -> str:
    return re.sub(r"\s+", " ", str(text or "")).strip()


def hangul_ratio(text: str) -> float:
    letters = LETTER_RE.findall(str(text or ""))
    if not letters:
        return 0.0
    ko = HANGUL_RE.findall(str(text or ""))
    return float(len(ko)) / float(max(1, len(letters)))


def iter_jsonl_sample(path: Path, max_lines: int) -> Iterable[Dict]:
    with path.open("r", encoding="utf-8", errors="replace") as f:
        for i, line in enumerate(f, 1):
            if i > int(max_lines):
                break
            s = line.strip()
            if not s:
                continue
            try:
                obj = json.loads(s)
            except json.JSONDecodeError:
                continue
            if isinstance(obj, dict):
                yield obj


def load_json_sample(path: Path) -> Dict:
    try:
        with path.open("r", encoding="utf-8", errors="replace") as f:
            obj = json.load(f)
        if isinstance(obj, dict):
            return obj
    except Exception:
        pass
    return {}


def analyze_sample(path: Path, probe_lines: int) -> Dict:
    ext = path.suffix.lower()
    parsed = 0
    chat_like = 0
    code_like = 0
    ko_hint = 0
    key_counter = Counter()

    if ext == ".jsonl":
        for row in iter_jsonl_sample(path, max_lines=int(probe_lines)):
            parsed += 1
            for k in row.keys():
                key_counter[k] += 1
            inp = normalize_space(str(row.get("input", row.get("instruction", row.get("prompt", "")))))
            out = normalize_space(str(row.get("output", row.get("response", row.get("answer", "")))))
            if inp and out:
                chat_like += 1
            text = f"{inp}\n{out}"
            if CODE_HINT_RE.search(text):
                code_like += 1
            lang = str(row.get("language", row.get("segment_tag", row.get("task_type", "")))).strip().lower()
            if lang in {"ko", "korean"} or hangul_ratio(text) >= 0.16:
                ko_hint += 1
    else:
        row = load_json_sample(path)
        if row:
            parsed = 1
            key_counter.update(row.keys())
            text = normalize_space(json.dumps(row, ensure_ascii=False)[:4000])
            if CODE_HINT_RE.search(text):
                code_like = 1
            if hangul_ratio(text) >= 0.16:
                ko_hint = 1

    return {
        "parsed": int(parsed),
        "chat_ratio": float(chat_like / parsed) if parsed else 0.0,
        "code_ratio": float(code_like / parsed) if parsed else 0.0,
        "ko_ratio": float(ko_hint / parsed) if parsed else 0.0,
        "top_keys": [k for k, _ in key_counter.most_common(10)],
    }


def classify(rel_posix: str, sample: Dict) -> Tuple[str, float, List[str]]:
    s = rel_posix.lower()
    reasons: List[str] = []

    if s.endswith(".manifest.json"):
        return "profile_meta", 0.65, ["manifest metadata file"]

    if "/by_field/" in s:
        return "other", 0.5, ["already generated by-field artifact"]

    if "/quality/" in s and "hq_ko_chat" in s:
        return "quality_curated", 0.95, ["quality curated dataset path"]

    if "session_memory" in s or "continual_buffer" in s:
        return "memory_continual", 0.95, ["memory/buffer keyword"]

    if "ccl_specbook" in s:
        return "ccl_specbook", 0.98, ["ccl_specbook keyword"]

    if "logic_reasoning" in s or "expert_reasoning" in s:
        return "reasoning_logic", 0.95, ["reasoning keyword"]

    if "translation" in s:
        return "translation_multilingual", 0.95, ["translation keyword"]

    if "router" in s:
        return "routing_intent", 0.95, ["router keyword"]

    if "identity" in s or "name_patch" in s or "name_disambig" in s:
        return "identity_persona", 0.96, ["identity/name keyword"]

    if "stages_" in s or "turbo_stage4" in s:
        return "stage_curriculum", 0.96, ["stage curriculum keyword"]

    if "syllable_profile" in s or "profile" in s:
        return "profile_meta", 0.9, ["profile/meta keyword"]

    if "ko_clean" in s or "fluency" in s or "ratio_boost" in s or "pure_ko" in s or "ultra_targeted" in s:
        return "ko_fluency_targeted", 0.92, ["ko fluency keyword"]

    if "code" in s or sample.get("code_ratio", 0.0) >= 0.35:
        reasons.append("code keyword or high code ratio")
        if "chat" in s and "ko" in s:
            reasons.append("chat/ko keyword")
            return "coding_tech", 0.88, reasons

    if "mit_" in s or "slm_mit" in s or "hf_mit" in s or "lang_blend" in s or "domain_mix" in s or "unified" in s:
        return "foundation_mit_mix", 0.9, ["mit/mix keyword"]

    if "capability_pack" in s:
        if sample.get("code_ratio", 0.0) >= 0.22:
            return "coding_tech", 0.76, ["capability pack with code-like rows"]
        return "reasoning_logic", 0.72, ["capability pack default to reasoning"]

    if "kullm" in s:
        if sample.get("code_ratio", 0.0) >= 0.25:
            return "coding_tech", 0.72, ["kullm with code signal"]
        return "ko_chat_general", 0.72, ["kullm general chat keyword"]

    if "chat" in s or "auto_gold" in s or "intro_override" in s:
        if sample.get("code_ratio", 0.0) >= 0.28:
            return "coding_tech", 0.75, ["chat dataset with high code ratio"]
        return "ko_chat_general", 0.82, ["chat/general keyword"]

    if sample.get("code_ratio", 0.0) >= 0.45:
        return "coding_tech", 0.6, ["fallback by code ratio"]

    if sample.get("ko_ratio", 0.0) >= 0.6 and sample.get("chat_ratio", 0.0) >= 0.5:
        return "ko_chat_general", 0.58, ["fallback by ko+chat ratio"]

    return "other", 0.5, ["no strong keyword/signal"]


def write_field_lists(out_dir: Path, grouped: Dict[str, List[Dict]]) -> None:
    for field, items in grouped.items():
        field_dir = out_dir / field
        field_dir.mkdir(parents=True, exist_ok=True)

        lines = [
            f"# {field} - {FIELD_DESC.get(field, '')}",
            "",
            "path\tsize_bytes\tconfidence\treason",
        ]
        for item in sorted(items, key=lambda x: x["path"]):
            reason = "; ".join(item.get("reasons", []))
            lines.append(
                f'{item["path"]}\t{item["size_bytes"]}\t{item["confidence"]:.2f}\t{reason}'
            )
        (field_dir / "FILES.tsv").write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> None:
    args = parse_args()
    data_root = Path(args.data_root)
    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    target_ext = tuple(dict.fromkeys([e.lower() if e.startswith(".") else f".{e.lower()}" for e in args.ext]))

    files: List[Path] = []
    for p in sorted(data_root.rglob("*")):
        if not p.is_file():
            continue
        rel = p.relative_to(data_root).as_posix()
        if rel.startswith("by_field/"):
            continue
        if p.suffix.lower() not in target_ext:
            continue
        if rel.endswith(".manifest.json"):
            continue
        files.append(p)

    index_rows: List[Dict] = []
    grouped: Dict[str, List[Dict]] = defaultdict(list)
    field_counter = Counter()
    field_size_counter = Counter()

    for p in files:
        rel = p.relative_to(data_root).as_posix()
        sample = analyze_sample(path=p, probe_lines=int(args.probe_lines))
        field, confidence, reasons = classify(rel_posix=rel, sample=sample)

        row = {
            "path": rel,
            "size_bytes": int(p.stat().st_size),
            "field": field,
            "field_desc": FIELD_DESC.get(field, ""),
            "confidence": float(confidence),
            "reasons": reasons,
            "sample": sample,
        }
        index_rows.append(row)
        grouped[field].append(row)
        field_counter[field] += 1
        field_size_counter[field] += int(p.stat().st_size)

    index_rows.sort(key=lambda x: (x["field"], x["path"]))
    write_field_lists(out_dir=out_dir, grouped=grouped)

    index_payload = {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "data_root": str(data_root),
        "out_dir": str(out_dir),
        "dataset_count": int(len(index_rows)),
        "fields": FIELD_DESC,
        "datasets": index_rows,
    }
    summary_payload = {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "dataset_count": int(len(index_rows)),
        "field_counts": dict(field_counter),
        "field_size_bytes": dict(field_size_counter),
        "field_size_mb": {k: round(v / (1024 * 1024), 3) for k, v in field_size_counter.items()},
    }

    (out_dir / "dataset_field_index_v1.json").write_text(
        json.dumps(index_payload, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    (out_dir / "field_summary_v1.json").write_text(
        json.dumps(summary_payload, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )

    print(
        json.dumps(
            {
                "out_dir": str(out_dir),
                "dataset_count": int(len(index_rows)),
                "field_counts": dict(field_counter),
            },
            ensure_ascii=False,
        )
    )


if __name__ == "__main__":
    main()
